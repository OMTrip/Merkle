export function parseCustomDate(dateString) {
  const months = {
    January: 0,
    February: 1,
    March: 2,
    April: 3,
    May: 4,
    June: 5,
    July: 6,
    August: 7,
    September: 8,
    October: 9,
    November: 10,
    December: 11,
  };

  const dateParts = dateString.split(/[, ]+/);
  const month = months[dateParts[0]];
  const day = parseInt(dateParts[1]);
  const year = parseInt(dateParts[2]);
  const timeParts = dateParts[3].split(':');
  const hour = parseInt(timeParts[0]);
  const minute = parseInt(timeParts[1]);
  const ampm = dateParts[4] === 'AM' ? 'AM' : 'PM';

  let date = new Date(year, month, day, hour, minute);

  if (ampm === 'PM') {
    date.setHours(date.getHours() + 12);
  }

  return date.getTime();
}

export function formatDateFromTimestamp(timestamp) {
  // Convert the timestamp to milliseconds by multiplying it by 1000
  const timestampInMilliseconds = timestamp * 1000;

  // Create a new Date object using the timestamp in milliseconds
  const date = new Date(timestampInMilliseconds);

  // Get the month, day, and year components
  const options = { year: 'numeric', month: 'long', day: 'numeric' };
  const formattedDate = date.toLocaleDateString(undefined, options);

  return formattedDate;
}

